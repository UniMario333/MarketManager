   public static void main(String args[]) {
       
        SplashScreenView mySplash = new SplashScreenView();
        mySplash.setVisible(true);
        
        Connection connect = null;
        
        try {
            for(int i = 0; i <= 100; i++) {
                
                Thread.sleep(10);
                mySplash.loadingBar.setValue(i);
                mySplash.loadingNumber.setText(Integer.toString(i) + "%");
                
            }
            
            connect = Connect.connectDB();
            
            if(connect == null) {
                
                System.exit(-1);
                
            }
            
            new LoginView().setVisible(true);

            mySplash.dispose();

        }
        
        catch(InterruptedException e) {
            
            LOGGER.throwing(SplashScreenView.class.getName(), "main", e);
            
        }
        
    }
   

    private void categoryTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_categoryTableMouseClicked
        
        this.categoryTable.setDefaultEditor(Object.class, null);
        
        int rowIndexPK = categoryTable.getSelectedRow();
        
        Category category = categoryList.get(rowIndexPK);

        this.catName.setText(category.getName());
        this.catDesc.setText(category.getDescription());
        
        this.clickName = catName.getText();
        
    }//GEN-LAST:event_categoryTableMouseClicked

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        try {
            
            this.categoryController.addPressed(catName.getText(), catDesc.getText());
            
            this.categoryList = categoryController.initTable();
            
            this.populateTable();
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(this, "ERRORE: categoria già esistente");
            
            Logger.getLogger(CategoryView.class.getName()).log(Level.SEVERE, null, ex);
            
        } catch (StringEmptyException ex) {
            
            JOptionPane.showMessageDialog(this, ex.getMessage());
            
            Logger.getLogger(CategoryView.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        
        try {
            
            this.categoryController.updatePressed(catName.getText(), catDesc.getText(), clickName);
            
            this.categoryList = categoryController.initTable();
            
            this.populateTable();
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(this, "ERRORE: categoria già esistente");
            
            Logger.getLogger(CategoryView.class.getName()).log(Level.SEVERE, null, ex);
            
        } catch (StringEmptyException ex) {
            
            JOptionPane.showMessageDialog(this, ex.getMessage());
            
            Logger.getLogger(CategoryView.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        try {
            
            this.categoryController.deletePressed(catName.getText());
            
            this.categoryList = categoryController.initTable();
            
            this.populateTable();
            
        } catch (SQLException ex) {
            
            Logger.getLogger(CategoryView.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed


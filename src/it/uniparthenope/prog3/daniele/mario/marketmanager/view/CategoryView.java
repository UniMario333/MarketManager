    private void clearTextFieldCategory() {
        
        this.catName.setText("");
        this.catDesc.setText("");
        
    }
    
    /** Popola la tabella della finestra */
    public void populateTable() throws SQLException {
        
        DefaultTableModel tblModel = (DefaultTableModel)categoryTable.getModel();
                 
        tblModel.setRowCount(0);
        
        this.categoryTable.setDefaultEditor(Object.class, null);
        
        Integer columns = 2;
        
        Object[] row = new Object[columns];
        
        for(int indice = 0; indice < categoryList.size(); indice++){
            
            row[0] = categoryList.get(indice).getName();
            row[1] = categoryList.get(indice).getDescription();
            
            tblModel.addRow(row);
            
        }
        
    }
    
